DECLARE
  bad INT;
BEGIN
  SELECT COUNT(*) INTO bad
    FROM hq_products p
    JOIN hq_categories c ON c.id = p.category_id
   WHERE fn_category_scene(p.category_id) IN (1, 2)
     AND p.deleted_at IS NULL
     AND (p.unit IS NULL OR p.unit = '' OR c.level <> 3);
